function signUpForm(){
    var x = document.getElementById("main");
    x.style.display= "block";
}
function closeForm(){
    var x = document.getElementById("main");
    x.style.display= "none";
}
function hideCustom(){
    var x = document.querySelector(".custom-main")
    x.style.display="none";
}
function showCustom(){
    var x = document.querySelector(".custom-main")
    x.style.display="block";
}